# How many hours in a year?
# How many minutes in a decade?
# How many seconds old am I?

hours_year = 24 * 365
puts "#{hours_year} hours in one year."

minutes_decade = hours_year * 10 * 60
puts "#{minutes_decade} minutes in a decade."

age_seconds = hours_year * 27 * 60 * 60
puts "I am #{age_seconds} old."
